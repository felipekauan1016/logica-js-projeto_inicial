// crie uma funçao com um parametro em que o usuario digita uma letra e o sistema retorna um alert se ele é uma vogal ou nao.
function isVogal (letra) {
   
    if (letra === ('a') || letra === ('e') || letra === ('i') || letra === ('o') || letra === ('u')) {
        alert("A letra digitada é uma vogal.");
    } else {
        alert("A letra digitada não é uma vogal.");
    }
}

