//crie um programa que o usuario entra com dois valores e o sistema soma e imprime em um alert.

// Solicita ao usuário que insira dois valores
let valor1 = parseInt (prompt ("digite seu primeiro valor"));
let valor2 = parseInt (prompt ("digite seu segundo valor"));

// Calculando a soma
let soma = (valor1 + valor2);

//crie um alert para aparecer a soma dos valores
alert (`seu resultado é: ${soma}`);